CSS Selector :
-	elementA + elementB : Select elementB ngay sau thẻ đóng elementA
-	elementA elementB : Select tất cả elementB nằm trong elementA
-	elementA > elementB : Select tất cả elementB được bao trực tiếp (parent) bởi elementA
-	elementA ~ elementB : Select tất cả elementB nằm dưới elementA trong tập HTML
-	element[attributeA]: Select các element có attributeA
-	[attribute ~= valueA] : Select các element có attribute chứa valueA
-	[attribute |= valueA] : Select các element có gía trị bắt đầu bằng valueA
-	[attribute ^= valueA] : 
-	[attribute $= valueA] : Select các element có gía trị kết thúc bằng valueA

